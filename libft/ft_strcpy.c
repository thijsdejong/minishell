#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int len;
	int i;

	len = ft_strlen(src);
	i = 0;
	while (i < len)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
